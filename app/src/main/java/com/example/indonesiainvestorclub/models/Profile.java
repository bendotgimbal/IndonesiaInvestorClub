package com.example.indonesiainvestorclub.models;

public class Profile {
    private String firstname;
    private String lastname;
    private String dob;
    private String gender;
    private String maritasstatus;
    private String nationality;
    private String address;
    private String city;
    private String postalcode;
    private String country;
    private String phoneno;
    private String ocupation;

    public String getFirstName() {
        return firstname;
    }

    public void setFirstName(String firstname) {
        this.firstname = firstname;
    }

    public String getLastName() {
        return lastname;
    }

    public void setLastName(String lastname) {
        this.lastname = lastname;
    }

    public String getDoB() {
        return dob;
    }

    public void setDoB(String dob) {
        this.dob = dob;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getMaritasStatus() {
        return maritasstatus;
    }

    public void setMaritasStatus(String maritasstatus) {
        this.maritasstatus = maritasstatus;
    }

    public String getNationality() {
        return nationality;
    }

    public void setNationality(String nationality) {
        this.nationality = nationality;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getPostalCode() {
        return postalcode;
    }

    public void setPostalCode(String postalcode) {
        this.postalcode = postalcode;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getPhoneNo() {
        return phoneno;
    }

    public void setPhoneNo(String phoneno) {
        this.phoneno = phoneno;
    }

    public String getOccupation() {
        return ocupation;
    }

    public void setOccupation(String ocupation) {
        this.ocupation = ocupation;
    }
}
